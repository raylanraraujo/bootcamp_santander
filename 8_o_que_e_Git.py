#   git commit: grava as alteracoes no repositorio
#   git pull: para atualizar o seu repositorio local a partir repositório que ta no servidor. Puxa as alteracoes do repositorio remoto para o repositório local e mesca com o arquivo que voce ta trabalhando
#   git push: que ele emporra as alterações do repositorio local para o remoto.

